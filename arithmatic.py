import numpy as np
arr=np.array([10,20,30])
print(arr)

# Mathametical Operations.........

# Additions.......
add=arr + 10
print("Additions=)",add)

# Substraction.........
sub=arr - 10
print("Substraction=)",sub)

# Multiplections........
mul=arr * 10
print("Multiplecation=)",mul)

# Division..............
div=arr / 2
print("Division=)",div)

# Sum of numbers.............
xyz=np.array([[1,2,3],[4,5,6]])
s=np.sum(xyz)
print("Sum is =",s)

# minimum numbers.............
xyz=np.array([[1,2,3],[4,5,6]])
m=np.min(xyz)
print("minimum number is =",m)

# Maximum number.............
xyz=np.array([[1,2,3],[4,5,6]])
mx=np.max(xyz)
print("maximum number is =",mx)